
//toggle 3
let deactivate2 = document.getElementById("toggle3");
let red1 = document.getElementById('red1');

deactivate2.addEventListener("click", toggleCircle3);

function toggleCircle3() {
    if (red1.className == "t3") {
        red1.classList.remove("t3");
        red1.classList.add("t2");
    } else {
        red1.classList.toggle("t3");
    }
}



